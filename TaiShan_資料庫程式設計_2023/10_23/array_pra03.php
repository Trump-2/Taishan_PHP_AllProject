<h1>威力彩電腦選號沒有重複號碼 (利用 while 迴圈)</h1>
<ul>
  <li>使用亂數函式rand($a,$b)來產生號碼</li>
  <li>將產生的號碼順序存入陣列中</li>
  <li>每次存入陣列中時會先檢查陣列中的資料有沒有重覆</li>
  <li>完成選號後將陣列內容印出</li>
</ul>

<?php
$area1 = [];
while (count($area1) < 6) {
  $temp = rand(1, 38);
  if (!in_array($temp, $area1))
    $area1[] = $temp;
}

// foreach 語法中 as 後面可以只指定 value 也可以指定 key / value
foreach ($area1 as $value) {
  echo $value . ",";
}


?>


